def greet(name: str) -> str:
    """Return greeting.

    Args:
        name: Person to greet.

    Returns:
        Greeting string.
    """
    return f"Hello, {name}!! Welcome!"


def farewell(name: str) -> str:
    """Return farewell.

    Args:
        name: Person to say farewell to.

    Returns:
        Farewell string.
    """
    return f"Goodbye, {name}! See you soon!"
